#include <unistd.h>

int	main(int argc, char *argv[])
{
	int i;
	int j;

	i = 0;
	j = argc - 1;
	while (j > 0)
	{
		while (argv[j][i])
		{
			write(1, &argv[j][i], 1);
			i++;
		}
		i = 0;
		j--;
		write(1, "\n", 1);
	}
	return (0);
}


